pub enum Commands {
    SetLED(u8),
    SendVibration,
    SaveMac1([u8;6]),
    SaveMac2,
    SaveMac3,
    SaveMac4,
}

impl Commands {
    pub fn to_bytes(self) -> Vec<u8> {
        match self {
            Commands::SetLED(led) => {
                Vec::from([
                    0x09, 0x91, 0x00, 0x07, 0x00, 0x08, 0x00, 0x00, led, 0x00,
                    0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
                ])
            }
            Commands::SendVibration => {
                Vec::from([
                    0x0A, 0x91, 0x01, 0x02,
                    0x00, 0x04, 0x00, 0x00,
                    0x03, 0x00, 0x00, 0x00
                ])
            }
            Commands::SaveMac1(mac) => {
                let mut data = Vec::from([
                    0x15,0x91,0x01,0x01,0x00,0x0e,0x00,0x00,0x00,0x01
                ]);
                data.extend_from_slice(&mac);
                data
            }
            Commands::SaveMac2 => {
                Vec::from([
                    0x15,0x91,0x01,0x04,0x00,0x11,0x00,0x00,0x00,0x08,0x06,0x5a,0x60,0xe9,0x02,0xe4,0xe1,0x02,0x02,0x9e,0x3f,0xa3,0x9a,0x78,0xd1
                ])
            }
            Commands::SaveMac3 => {
                Vec::from([
                    0x15,0x91,0x01,0x02,0x00,0x11,0x00,0x00,0x00,0x93,0x4e,0x58,0x0f,0x16,0x3a,0xee,0xcf,0xb5,0x75,0xfc,0x91,0x36,0xb2,0x2f,0xbb
                ])
            }
            Commands::SaveMac4 => {
                Vec::from([
                    0x15,0x91,0x01,0x03,0x00,0x01,0x00,0x00,0x00
                ])
            }
        }
    }
}